import mongoose from "mongoose";

const commentSchema = new mongoose.Schema({
    commentText: {
        type: String,
        required: true,
    },
    userId: {
        type: mongoose.Schema.Types.ObjectId,
        ref: 'User',
        required: true,
    },
    postId: {
        type: mongoose.Schema.Types.ObjectId,
        ref: 'Post',
        required: true,
    },
    createdAt: {
        type: Date,
        default: Date.now,
    },
});

export const commentModel= mongoose.model('Comment', commentSchema);

